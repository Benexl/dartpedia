import 'colors.dart';

class Theme {
  final Color background;
  final Color foreground;
  final Color boldAccent;
  final Color subtleAccent;
  final Color error;

  const Theme({
    required this.background,
    required this.foreground,
    required this.boldAccent,
    required this.subtleAccent,
    required this.error,
  });
}
